package com.goooldden.haaannuman.jiqwqe;

public class Cssad {

    private String url;
    private String keyDefault;
    private String fbId;

    public Cssad(String url, String keyDefault, String fbId) {
        this.url = url;
        this.keyDefault = keyDefault;
        this.fbId = fbId;
    }

    public String getUrl() {
        return url;
    }

    public String getKeyDefault() {
        return keyDefault;
    }

    public String getFbId() {
        return fbId;
    }
}
